package oliweb.nc.oliweb.utility;

public class MediaUtilityException extends RuntimeException {
    public MediaUtilityException() {
    }

    public MediaUtilityException(String message) {
        super(message);
    }

    public MediaUtilityException(String message, Throwable cause) {
        super(message, cause);
    }

    public MediaUtilityException(Throwable cause) {
        super(cause);
    }
}
